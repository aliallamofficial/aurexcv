    function initializeVoiceDictation(btnId, targetInputId) {
        const btn = document.getElementById(btnId);
        const input = document.getElementById(targetInputId);
        if (!btn || !input) return;

        const SpeechRecognition = window.SpeechRecognition || window.webkitSpeechRecognition;
        if (!SpeechRecognition) {
            btn.style.display = "none"; // إخفاء الميزة في المتصفحات التي لا تدعمها كلياً
            return;
        }

        const recognition = new SpeechRecognition();
        recognition.continuous = false;
        
        // ضبط لغة التعرف تلقائياً مع لغة المنصة لضمان دقة الإملاء
        recognition.lang = currentLang === 'ar' ? 'ar-SA' : (currentLang === 'fr' ? 'fr-FR' : 'en-US');
        recognition.interimResults = false;

        recognition.onstart = () => {
            btn.innerText = currentLang === 'ar' ? "🎙️ جاري الاستماع..." : (currentLang === 'fr' ? "🎙️ Écoute..." : "🎙️ Listening...");
            btn.style.borderColor = "var(--accent-pink)";
            btn.style.color = "var(--accent-pink)";
        };

        recognition.onerror = () => { resetVoiceBtnState(btn); };
        recognition.onend = () => { resetVoiceBtnState(btn); };

        recognition.onresult = (event) => {
            const transcript = event.results[0][0].transcript;
            if(input.tagName === "INPUT") {
                input.value = transcript;
            } else {
                input.value += (input.value ? " " : "") + transcript;
            }
            calculateAtsScoreLocally();
        };

        btn.addEventListener("click", () => {
            try { recognition.start(); } catch(e) { recognition.stop(); }
        });
    }

