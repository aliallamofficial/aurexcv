// تحديد اللغة الافتراضية بناءً على المتصفح أو الاختيار المحفوظ
let currentLang = localStorage.getItem("ali_cv_lang") || "ar";

function applyLanguage(lang) {
    currentLang = lang;
    localStorage.setItem("ali_cv_lang", lang);
    
    // تحديث اتجاه لوحة العرض واللغة العامة للهيكل
    const htmlEl = document.getElementById("appHtml");
    if(lang === 'ar') {
        htmlEl.setAttribute("dir", "rtl");
        htmlEl.setAttribute("lang", "ar");
    } else {
        htmlEl.setAttribute("dir", "ltr");
        htmlEl.setAttribute("lang", lang);
    }

    // ترجمة كافة العناصر الحاملة للوسم data-i18n
    document.querySelectorAll("[data-i18n]").forEach(el => {
        const key = el.getAttribute("data-i18n");
        if(translations[lang] && translations[lang][key]) {
            if(el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
                el.setAttribute("placeholder", translations[lang][key]);
            } else {
                el.innerText = translations[lang][key];
            }
        }
    });

    // تحديث التلميحات ونصوص المساعدة المتقدمة ديناميكياً
    updateDynamicPlaceholdersAndTips(lang);
}

