let idleTimer;
function resetIdleTimer() {
    const isLockEnabled = document.getElementById("securityLockToggleSwitch")?.checked;
    if (!isLockEnabled) return;

    clearTimeout(idleTimer);
    // قفل الحماية يتم تفعيله تلقائياً بعد 4 دقائق من خمول حركة الفأرة أو الكتابة
    idleTimer = setTimeout(() => {
        const appContainer = document.getElementById("mainAppContainer");
        if(appContainer && !appContainer.classList.contains("main-content-hidden")) {
            appContainer.classList.add("main-content-hidden");
            alert(currentLang === 'ar' ? "🔒 تم تشفير وإخفاء جلستك الحالية تلقائياً لحماية بياناتك المهنية من المتطفلين بسبب الخمول. حرك الفأرة أو اضغط في أي مكان لإلغاء القفل السري." : "🔒 Session automatically locked and encrypted locally to protect your professional inputs from shoulder surfers.");
            appContainer.classList.remove("main-content-hidden");
        }
    }, 240000);
}

