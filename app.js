        btn.type = "button";
        btn.className = "sug-badge-btn";
        btn.style.cssText = "background: rgba(255,255,255,0.03); border: 1px solid rgba(255,255,255,0.06); padding: 6px 12px; border-radius: 8px; color: #e5e7eb; font-size: 12px; cursor: pointer; transition: all 0.2s;";
        
