// 🔀 إدارة الروابط المشتركة سحابياً (Cloud Deep Links Router)
// ========================================================
function checkSharedViewRoute() {
    const urlParams = new URLSearchParams(window.location.search);
    const sharedDataParam = urlParams.get("share");
    
    if (sharedDataParam) {
        try {
            // فك تشفير البيانات المهنية القادمة من الرابط السحابي
            const decodedText = decodeURIComponent(atob(sharedDataParam));
            const parsedData = JSON.parse(decodedText);
            
            // إدخال البيانات المفكوكة في صناديق العرض والمعاينة مباشرة
            if (parsedData.title) document.getElementById("targetJobTitleInput").value = parsedData.title;
            if (parsedData.cv) document.getElementById("currentCvTextInput").value = parsedData.cv;
            if (parsedData.output) document.getElementById("outputBox").value = parsedData.output;
            
            // تفعيل شريط الإشعار العلوي الخاص بوضع العرض فقط
            const banner = document.getElementById("sharedViewBanner");
            if (banner) banner.classList.remove("hidden");
            
            calculateAtsScoreLocally();
        } catch (e) {
            console.error("خطأ في معالجة وفك تشفير الرابط السحابي المشترك:", e);
        }
    }
}

