// ⚡ نظام فحص وتحليل الـ ATS اللحظي (Local ATS Engine)
// ========================================================
function calculateAtsScoreLocally() {
    const jobTitle = document.getElementById("targetJobTitleInput").value.trim();
    const currentCv = document.getElementById("currentCvTextInput").value.trim();
    const jobDesc = document.getElementById("jobDescriptionTextInput").value.trim();
    
    let score = 0;
    let lengthScore = 0;
    let keywordScore = 0;
    let structureScore = 0;
    let contactScore = 0;

    // 1️⃣ فحص حجم ومدى ثراء النص
    const totalLength = currentCv.length + jobTitle.length;
    if(totalLength > 800) { lengthScore = 25; }
    else if(totalLength > 400) { lengthScore = 18; }
    else if(totalLength > 100) { lengthScore = 10; }

    // 2️⃣ فحص الكلمات المفتاحية الاستراتيجية
    if(jobTitle.length > 3) {
        keywordScore += 10;
        // محاكاة فحص تقاطع الكلمات المفتاحية بين السيرة والوصف الوظيفي
        if(jobDesc.length > 10) {
            const words = jobTitle.toLowerCase().split(/\s+/);
            let matchCount = 0;
            words.forEach(w => { if(jobDesc.toLowerCase().includes(w)) matchCount++; });
            if(matchCount > 0) keywordScore += 15;
        } else {
            // مكافأة ثراء النص العام في غياب الوصف الإعلاني
            if(currentCv.toLowerCase().includes(jobTitle.toLowerCase())) keywordScore += 10;
        }
    }

    // 3️⃣ فحص هيكلة الأقسام القياسية للـ ATS
    const lowercaseCv = currentCv.toLowerCase();
    const structureKeywords = [
        "خبرة", "تعليم", "مهارات", "مشاريع", "لغات",
        "experience", "education", "skills", "projects", "languages", "summary",
        "formations", "compétences", "langues", "profil"
    ];
    let sectionsFound = 0;
    structureKeywords.forEach(kw => { if(lowercaseCv.includes(kw)) sectionsFound++; });
    structureScore = Math.min(25, sectionsFound * 5);

    // 4️⃣ فحص بيانات الاتصال الأساسية لضمان تواصل مسؤولي التوظيف
    if(lowercaseCv.includes("@") || lowercaseCv.includes("بريد") || lowercaseCv.includes("email")) contactScore += 13;
    if(/[0-9]{7,}/.test(lowercaseCv) || lowercaseCv.includes("هاتف") || lowercaseCv.includes("phone") || lowercaseCv.includes("tel")) contactScore += 12;

    // المجموع الكلي النهائي لمقياس الـ ATS
    score = lengthScore + keywordScore + structureScore + contactScore;
    
    // تحديث العناصر المرئية في الواجهة فوراً
    document.getElementById("atsScoreValueDisplay").innerText = `${score}%`;
    document.getElementById("atsScoreProgressBarFill").style.width = `${score}%`;

    // تحديث ألوان مؤشرات التقدم الأربعة
    updateIndicatorStatus("indLength", lengthScore, 20);
    updateIndicatorStatus("indKeywords", keywordScore, 20);
    updateIndicatorStatus("indStructure", structureScore, 20);
    updateIndicatorStatus("indContact", contactScore, 20);

    // تحديث نص الحالة الإرشادي بناءً على الدرجة الحالية
    const statusTextEl = document.getElementById("atsScoreStatusText");
    if(score === 0) {
        statusTextEl.innerText = translations[currentLang].atsInitialStatusText;
    } else if(score < 45) {
        statusTextEl.innerText = currentLang === 'ar' ? "⚠️ السيرة ضعيفة جداً وتفتقر للمصطلحات الهيكلية، قد يتم تصفيتها تلقائياً بواسطة أنظمة الـ ATS." : (currentLang === 'en' ? "⚠️ Critically low score. The resume lacks standard formatting structures and will likely be filtered out." : "⚠️ Score critique. Le CV manque de structures standards et sera probablement filtré.");
    } else if(score < 75) {
        statusTextEl.innerText = currentLang === 'ar' ? "⚡ أداء جيد، ولكنها تحتاج لدمج المزيد من الكلمات الاستراتيجية وتفصيل الإنجازات لتخطي فلاتر الشركات الكبرى." : (currentLang === 'en' ? "⚡ Good progress, but needs key terminology blending and measurable metric enhancements." : "⚡ Progression correcte, mais nécessite l'intégration de termes clés et de résultats chiffrés.");
    } else {
        statusTextEl.innerText = currentLang === 'ar' ? "🎉 سيرة ممتازة ومثالية! الهيكلة والبيانات متطابقة تماماً مع خوارزميات التوظيف الحديثة وجاهزة للمنافسة العالمية." : (currentLang === 'en' ? "🎉 Excellent ATS compliance! Structure and keyword densities are highly optimized for enterprise recruitment loops." : "🎉 Excellente conformité ATS ! La structure et la densité des mots-clés sont optimisées pour le recrutement.");
    }
}

function updateIndicatorStatus(id, score, threshold) {
    const el = document.getElementById(id);
    if(!el) return;
    if(score >= threshold) {
        el.style.borderColor = "rgba(34, 197, 94, 0.3)";
        el.style.background = "rgba(34, 197, 94, 0.05)";
        el.style.color = "#22c55e";
    } else if(score > 0) {
        el.style.borderColor = "rgba(234, 179, 8, 0.3)";
        el.style.background = "rgba(234, 179, 8, 0.05)";
        el.style.color = "#eab308";
    } else {
        el.style.borderColor = "rgba(255,255,255,0.04)";
        el.style.background = "rgba(255,255,255,0.01)";
        el.style.color = "var(--text-muted)";
    }
}

