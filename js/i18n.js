// 🌐 كائن الترجمة المحدث مع الـ Dynamic Fallback Proxies ودعم روابط الـ Deep Linking
window.AurexI18n = {
    currentLang: 'en',
    translations: aurexTranslations,
    
    get(key) {
        const dict = this.translations[this.currentLang] || {};
        return dict[key] || this.translations['en'][key] || key;
    },
    
    switchLanguage(lang) {
        if (!lang) return;
        this.currentLang = lang;
        const htmlTag = document.documentElement;
        
        // 1️⃣ إعداد اتجاه الصفحة والخطوط
        if (lang === 'ar') {
            htmlTag.setAttribute("dir", "rtl");
            htmlTag.setAttribute("lang", "ar");
            htmlTag.style.fontFamily = "'Cairo', sans-serif";
        } else {
            htmlTag.setAttribute("dir", "ltr");
            htmlTag.setAttribute("lang", lang);
            htmlTag.style.fontFamily = "'Inter', sans-serif";
        }

        // 2️⃣ إطلاق كلاس الـ RTL لتشغيل استجابة الـ CSS على الموبايل والشاشات
        document.body.classList.toggle('rtl', lang === 'ar');

        // 3️⃣ ترجمة عناصر الـ DOM المباشرة [data-i18n]
        document.querySelectorAll("[data-i18n]").forEach(element => {
            const key = element.getAttribute("data-i18n");
            element.textContent = this.get(key);
        });

        // 🔥 [V6.7 CRITICAL FIX] المزامنة الإجبارية لقسم المقالات (المدونة) لضمان ظهور النصوص باللغتين
        const blogTitle = document.querySelector('.blog-matrix-title');
        if (blogTitle) blogTitle.textContent = this.get('blogGlobalTitle');
        
        const blogDesc = document.querySelector('.blog-matrix-desc');
        if (blogDesc) blogDesc.textContent = this.get('blogGlobalDesc');

        const blogCards = document.querySelectorAll('.blog-article-card');
        if (blogCards.length >= 3) {
            // المقالة الأولى
            if (blogCards[0].querySelector('h3')) blogCards[0].querySelector('h3').textContent = this.get('blogAtsCardTitle');
            if (blogCards[0].querySelector('p')) blogCards[0].querySelector('p').textContent = this.get('blogAtsCardDesc');
            if (blogCards[0].querySelector('.blog-read-link')) blogCards[0].querySelector('.blog-read-link').textContent = this.get('readMoreBtn');
            
            // المقالة الثانية
            if (blogCards[1].querySelector('h3')) blogCards[1].querySelector('h3').textContent = this.get('blogExecCardTitle');
            if (blogCards[1].querySelector('p')) blogCards[1].querySelector('p').textContent = this.get('blogExecCardDesc');
            if (blogCards[1].querySelector('.blog-read-link')) blogCards[1].querySelector('.blog-read-link').textContent = this.get('readMoreBtn');

            // المقالة الثالثة
            if (blogCards[2].querySelector('h3')) blogCards[2].querySelector('h3').textContent = this.get('blogAiCardTitle');
            if (blogCards[2].querySelector('p')) blogCards[2].querySelector('p').textContent = this.get('blogAiCardDesc');
            if (blogCards[2].querySelector('.blog-read-link')) blogCards[2].querySelector('.blog-read-link').textContent = this.get('readMoreBtn');
        }

        // 4️⃣ ترجمة عناصر التلميحات [data-i18n-placeholder]
        document.querySelectorAll("[data-i18n-placeholder]").forEach(element => {
            const key = element.getAttribute("data-i18n-placeholder");
            element.setAttribute("placeholder", this.get(key));
        });

        // 5️⃣ تحديث كروت لوحة التحكم البصرية الرئيسية
        const cards = document.querySelectorAll('.aurex-card');
        if (cards.length >= 4) {
            if (cards[0].querySelector('.card-header-title')) cards[0].querySelector('.card-header-title').textContent = this.get('cardIdentityTitle');
            if (cards[1].querySelector('.card-header-title')) cards[1].querySelector('.card-header-title').textContent = this.get('cardExperienceTitle');
            if (cards[2].querySelector('.card-header-title')) cards[2].querySelector('.card-header-title').textContent = this.get('cardSkillsTitle');
            if (cards[3].querySelector('.card-header-title')) cards[3].querySelector('.card-header-title').textContent = this.get('cardEducationTitle');
        }

        // 6️⃣ تحديث حقول الحذف الفورية
        document.querySelectorAll('.exp-company').forEach(el => el.setAttribute("placeholder", this.get('phCompany')));
        document.querySelectorAll('.exp-role').forEach(el => el.setAttribute("placeholder", this.get('phRole')));
        document.querySelectorAll('.exp-desc').forEach(el => el.setAttribute("placeholder", this.get('phExpDesc')));
        document.querySelectorAll('.edu-school').forEach(el => el.setAttribute("placeholder", this.get('phSchool')));
        document.querySelectorAll('.edu-degree').forEach(el => el.setAttribute("placeholder", this.get('phDegree')));

        // 7️⃣ تحديث الـ Meta Tags فوراً لدعم الـ SEO
        const metaTitleEl = document.getElementById("metaTitle");
        if (metaTitleEl) metaTitleEl.textContent = this.get('metaTitle');
        
        const metaDescEl = document.querySelector('meta[name="description"]');
        if (metaDescEl) metaDescEl.setAttribute("content", this.get('metaDesc'));

        // 8️⃣ مزامنة الـ Select وحفظ الحالة محلياً
        const selector = document.getElementById('langSelector');
        if (selector) selector.value = lang;
        localStorage.setItem("aurex_preferred_lang", lang);

        window.dispatchEvent(new CustomEvent('aurexLanguageChanged', { detail: { lang } }));
    },

    // ⚡ محرك التمكين والتحقق الفوري من الروابط الذكية (Deep Linking Implementation)
    init() {
        const urlParams = new URLSearchParams(window.location.search);
        const urlLang = urlParams.get('lang');
        const savedLang = localStorage.getItem("aurex_preferred_lang") || 'en';
        
        // الأولوية القصوى لمعامل الرابط ?lang= ثم للمخزن المحلي
        this.switchLanguage(urlLang || savedLang);
    }
};

document.addEventListener("DOMContentLoaded", () => {
    window.AurexI18n.init();
});
