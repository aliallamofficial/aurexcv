// 🛠️ دالات التحكم الدقيقة في الأقسام مع حقن الـ Placeholders الديناميكية حسب اللغة الحالية
window.clearSectionData = function(sectionId) {
    if (sectionId === 'identity') {
        window.AurexUndoRegistry.identity = {
            name: document.getElementById('cvFullName')?.value || '',
            title: document.getElementById('cvTargetTitle')?.value || '',
            email: document.getElementById('cvEmail')?.value || '',
            phone: document.getElementById('cvPhone')?.value || ''
        };
        ['cvFullName', 'cvTargetTitle', 'cvEmail', 'cvPhone'].forEach(id => {
            const el = document.getElementById(id);
            if (el) el.value = '';
        });
    } else if (sectionId === 'skills') {
        window.AurexUndoRegistry.skills = document.getElementById('cvSkillsMatrix')?.value || '';
        const el = document.getElementById('cvSkillsMatrix');
        if (el) el.value = '';
    } else if (sectionId === 'experience') {
        window.AurexUndoRegistry.experience = document.getElementById('experienceNodesContainer')?.innerHTML || '';
        const el = document.getElementById('experienceNodesContainer');
        if (el) {
            el.innerHTML = `<div class="dynamic-node-block">
                <input type="text" class="aurex-input exp-company" placeholder="${window.AurexI18n.get('phCompany')}">
                <input type="text" class="aurex-input exp-role mt-10" placeholder="${window.AurexI18n.get('phRole')}">
                <textarea class="aurex-textarea mt-10 exp-desc" placeholder="${window.AurexI18n.get('phExpDesc')}"></textarea>
            </div>`;
        }
    } else if (sectionId === 'education') {
        window.AurexUndoRegistry.education = document.getElementById('educationNodesContainer')?.innerHTML || '';
        const el = document.getElementById('educationNodesContainer');
        if (el) {
            el.innerHTML = `<div class="dynamic-node-block">
                <input type="text" class="aurex-input edu-school" placeholder="${window.AurexI18n.get('phSchool')}">
                <input type="text" class="aurex-input edu-degree mt-10" placeholder="${window.AurexI18n.get('phDegree')}">
            </div>`;
        }
    }
    if (window.updateLivePreview) window.updateLivePreview();
    console.log(`[Registry] Section ${sectionId} cleared.`);
};

window.restoreSectionData = function(sectionId) {
    if (sectionId === 'identity' && window.AurexUndoRegistry.identity) {
        const data = window.AurexUndoRegistry.identity;
        if (document.getElementById('cvFullName')) document.getElementById('cvFullName').value = data.name;
        if (document.getElementById('cvTargetTitle')) document.getElementById('cvTargetTitle').value = data.title;
        if (document.getElementById('cvEmail')) document.getElementById('cvEmail').value = data.email;
        if (document.getElementById('cvPhone')) document.getElementById('cvPhone').value = data.phone;
    } else if (sectionId === 'skills' && window.AurexUndoRegistry.skills !== null) {
        const el = document.getElementById('cvSkillsMatrix');
        if (el) el.value = window.AurexUndoRegistry.skills;
    } else if (sectionId === 'experience' && window.AurexUndoRegistry.experience) {
        const el = document.getElementById('experienceNodesContainer');
        if (el) el.innerHTML = window.AurexUndoRegistry.experience;
    } else if (sectionId === 'education' && window.AurexUndoRegistry.education) {
        const el = document.getElementById('educationNodesContainer');
        if (el) el.innerHTML = window.AurexUndoRegistry.education;
    }
    if (window.updateLivePreview) window.updateLivePreview();
    console.log(`[Registry] Section ${sectionId} restored.`);
};

